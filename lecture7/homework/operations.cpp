#include<iostream>
using namespace std;
int main(){
    // AND operation
    cout << "AND operation" << endl;
    cout << "6 & 3 = " << (6 & 3) <<endl;

    // OR operation
    cout << "OR operation" << endl;
    cout << "6 | 10 = " << (6 | 10) << endl;

    // XOR operation
    cout << "XOR operation" << endl;
    cout << "6 ^ 10 = " << (6 ^ 10) << endl;

    // Left Shift Operation
    cout << "Left Shift Operation" << endl;
    cout << "10 << 2 = " << (10 << 2) << endl;

    // Right Shift Operation
    cout << "Right Shift Operation" << endl;
    cout << "10 >> 1 = " << (10 >> 1) <<endl;

}